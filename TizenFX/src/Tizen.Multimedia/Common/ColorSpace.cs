    {
        /// <summary>
        /// Y800.
        /// </summary>
        Y800,

        /// <summary>
        /// I420.
        /// </summary>
        I420,

        /// <summary>
        /// NV12.
        /// </summary>
        NV12,

        /// <summary>
        /// NV16.
        /// </summary>
        NV16,

        /// <summary>
        /// NV21.
        /// </summary>
        NV21,

        /// <summary>
        /// NV61.
        /// </summary>
        NV61,

