        /// YV12.
        /// </summary>
        ///
        YV12,

        /// <summary>
        /// YUYV.
        /// </summary>
        Yuyv,

        /// <summary>
        /// YUV422, alias for <see cref="Yuv422P"/>.
        /// </summary>
        Yuv422,

        /// <summary>
        /// YUV422P.
        /// </summary>
        Yuv422P = Yuv422,

        /// <summary>
        /// UYVY.
        /// </summary>
        Uyvy,

        /// <summary>
        /// RGB565.
        /// </summary>
        Rgb565,

        /// <summary>
        /// RGB888.
        /// </summary>
        Rgb888,

        /// <summary>
        /// RGBA8888.
        /// </summary>
        Rgba8888,

        /// <summary>
        /// ARGB8888.
        /// </summary>
        Argb8888,

        /// <summary>
        /// BGRA8888.
        /// </summary>
        Bgra8888,

        /// <summary>
        /// BGRX8888.
        /// </summary>
        Bgrx8888

